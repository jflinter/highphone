// Exports the `scores` table from the old Supabase project into a D1 seed file
// (migrations/0002_seed.sql). Read-only: uses the public anon key that the live
// site already ships with.
//
// Usage:  node scripts/export-supabase.mjs
//
// Videos in Supabase Storage are NOT handled here — that needs a service-role
// key (see scripts/export-videos.mjs).

import { writeFile } from 'node:fs/promises';

const SUPABASE_URL = 'https://hdfaiysbnbanqshkbamz.supabase.co';
const ANON_KEY =
  'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJpc3MiOiJzdXBhYmFzZSIsInJlZiI6ImhkZmFpeXNibmJhbnFzaGtiYW16Iiwicm9sZSI6ImFub24iLCJpYXQiOjE2ODM3NTY3NjQsImV4cCI6MTk5OTMzMjc2NH0.cz9KUNmBhsXDXu86gjFxPdX8OgRevuvBMbDi6XKT_j0';

const PAGE = 1000;
const OUT = 'migrations/0002_seed.sql';

const sqlStr = (v) =>
  v === null || v === undefined ? 'NULL' : `'${String(v).replace(/'/g, "''")}'`;
const sqlNum = (v) => (v === null || v === undefined ? 'NULL' : Number(v));
const sqlBool = (v) => (v ? 1 : 0);
// Normalize Postgres timestamptz to the same ISO-8601 millis+Z format the
// Worker writes, so lexical ordering / the 24h daily window stay consistent
// across old and new rows.
const sqlDate = (v) =>
  v === null || v === undefined ? 'NULL' : `'${new Date(v).toISOString()}'`;

const fetchPage = async (offset) => {
  const res = await fetch(
    `${SUPABASE_URL}/rest/v1/scores?select=*&order=id.asc&limit=${PAGE}&offset=${offset}`,
    { headers: { apikey: ANON_KEY, authorization: `Bearer ${ANON_KEY}` } }
  );
  if (!res.ok) throw new Error(`${res.status}: ${await res.text()}`);
  return res.json();
};

const main = async () => {
  const rows = [];
  for (let offset = 0; ; offset += PAGE) {
    const page = await fetchPage(offset);
    rows.push(...page);
    process.stderr.write(`fetched ${rows.length}\n`);
    if (page.length < PAGE) break;
  }

  // No explicit BEGIN/COMMIT: `wrangler d1 execute --remote` runs on Durable
  // Objects and rejects SQL transactions (it wraps the import atomically itself).
  const lines = [
    '-- Seed data exported from Supabase. Generated by scripts/export-supabase.mjs.',
  ];
  for (const r of rows) {
    lines.push(
      `INSERT INTO scores (id, local_id, player_id, player_name, duration_ms, has_case, created_at) VALUES (` +
        `${sqlNum(r.id)}, ${sqlStr(r.local_id)}, ${sqlStr(r.player_id)}, ` +
        `${sqlStr(r.player_name)}, ${sqlNum(r.duration_ms)}, ${sqlBool(r.has_case)}, ` +
        `${sqlDate(r.created_at)});`
    );
  }
  lines.push('');

  await writeFile(OUT, lines.join('\n'));
  process.stderr.write(`\nWrote ${rows.length} rows to ${OUT}\n`);
};

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
